let currentQuestion = 0;
let score = 0;

function loadQuestion() {
    const questionElement = document.getElementById('question');
    const optionsElement = document.getElementById('options');
    const progressElement = document.getElementById('progress');

    // Load the question text
    questionElement.innerText = aiQuizData[currentQuestion].question;

    // Load the options
    optionsElement.innerHTML = '';
    aiQuizData[currentQuestion].options.forEach((option, index) => {
        const li = document.createElement('li');
        li.innerText = option;
        li.onclick = () => selectOption(index);
        optionsElement.appendChild(li);
    });

    // Update progress bar
    const progressPercentage = ((currentQuestion + 1) / aiQuizData.length) * 100;
    progressElement.style.width = progressPercentage + '%';

    // Update button states
    document.getElementById('prev-btn').disabled = currentQuestion === 0;
    document.getElementById('next-btn').disabled = currentQuestion === aiQuizData.length - 1;

    // Show the finish button if it's the last question
    document.getElementById('finish-btn').style.display = currentQuestion === aiQuizData.length - 1 ? 'block' : 'none';
}

function selectOption(selectedIndex) {
    const correctAnswer = aiQuizData[currentQuestion].correctAnswer;
    if (selectedIndex === correctAnswer) {
        score++;
    }
    nextQuestion();
}

function nextQuestion() {
    if (currentQuestion < aiQuizData.length - 1) {
        currentQuestion++;
        loadQuestion();
    } else {
        showResult(); // Show result if it's the last question
    }
}

function prevQuestion() {
    if (currentQuestion > 0) {
        currentQuestion--;
        loadQuestion();
    }
}

function showResult() {
    const questionElement = document.getElementById('question');
    const optionsElement = document.getElementById('options');
    const progressElement = document.getElementById('progress');

    questionElement.innerText = `Quiz Finished! Your score: ${score} out of ${aiQuizData.length}`;
    optionsElement.innerHTML = ''; // Clear options
    progressElement.style.width = '100%'; // Full progress

    // Hide buttons
    document.getElementById('prev-btn').style.display = 'none';
    document.getElementById('next-btn').style.display = 'none';
    document.getElementById('finish-btn').style.display = 'none';
}

// Initial load
loadQuestion();